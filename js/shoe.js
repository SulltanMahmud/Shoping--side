function changeImage(color) {
  var img = document.getElementById("product-img");
  if (color === "green") {
    img.src = "./assets/sports-shoe-green.jpg";
  } else if (color === "orange") {
    img.src = "./assets/sports-shoe-red.jpg";
  } else if (color === "blue") {
    img.src = "./assets/blue-shoe.jpg";
  }
}

